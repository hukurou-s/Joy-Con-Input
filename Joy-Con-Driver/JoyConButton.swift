import Foundation

enum JoyConButton: Int32 {
    case A = 0x010000
    case B = 0x040000
    case X = 0x020000
    case Y = 0x080000
    case R = 0x004000
    case ZR = 0x008000
    case Plus = 0x000200
    case Home = 0x001000
    case SR = 0x200000
    case SL = 0x100000
    case Neutral = 0x3f000008
}
